package com.github.fgowiki.spider.model;

import javax.persistence.*;
import java.util.Objects;

/**
 * 功能描述：
 *
 * @author 蒋经
 * @version 1.0
 * @since 2017/12/15
 */
@Entity
@Table(name = "FGO_CAMP")
public class FgoCamp {
	private Integer id;
	private String name;

	@Id
	@Column(name = "ID")
	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	@Basic
	@Column(name = "NAME")
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public boolean equals(Object o) {
		if (this == o) return true;
		if (o == null || getClass() != o.getClass()) return false;
		FgoCamp fgoCamp = (FgoCamp) o;
		return id == fgoCamp.id &&
				Objects.equals(name, fgoCamp.name);
	}

	@Override
	public int hashCode() {

		return Objects.hash(id, name);
	}
}
